/*
 * 利用空闲检测中断时的回调函数来处理数据接收。
 * 注意：HAL_UARTEx_RxEventCallback 是在接收空闲后被调用，参数 Size 为本次接收的数据长度
 */
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
    if (huart->Instance == USART1)
    {
        // 确保接收到的数据以 '\0' 结尾
        if (Size < APP_RX_BUF_SIZE)
        {
            AppData[Size] = '\0';
        }
        else
        {
            AppData[APP_RX_BUF_SIZE - 1] = '\0';
        }

        //printf("Received: %s\r\n", AppData);

        // 检查是否包含 "dht11" 字符串
        if (strstr((char *)AppData, "dht11") != NULL)
        {
            dht11_print_request = 1;
        }
         if (strstr((char *)AppData, "data") != NULL)
        {
            dht11_read_request = 1;
        }
        HAL_UARTEx_ReceiveToIdle_DMA(huart, AppData, APP_RX_BUF_SIZE);
    }
}


